def _resolve(lang: str | None) -> str:
    code = ((lang or "").lower()[:2]) or DEFAULT_LANG
    return code if code in ABOUT_CONTACT_LANGS else DEFAULT_LANG


def get_about_ui(lang: str | None) -> dict:
    return dict(ABOUT_UI.get(_resolve(lang), ABOUT_UI[DEFAULT_LANG]))


def get_contact_ui(lang: str | None) -> dict:
    return dict(CONTACT_UI.get(_resolve(lang), CONTACT_UI[DEFAULT_LANG]))
