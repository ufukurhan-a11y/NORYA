                "fertilised egg implants in the uterus. Detecting measurable <strong>HCG levels</strong> "
                "in the blood is one of the earliest biochemical signs of pregnancy.</p>"
                "<p>Unlike urine-based pregnancy tests that give a simple positive or negative result, "
                "a <strong>beta HCG test</strong> measures the exact numerical concentration of the "
